function generate_tag(screen)
    symbols = { 'Ɣ', 'ʘ', 'Δ', 'δ', 'Γ', 'Ψ', 'Ω', 'ζ', 'μ',
	'σ', 'Ϟ', 'Ϡ', 'ϡ', 'Ϩ', 'Ю', 'ֆ', 'ջ', 'چ', 'ڤ', 'ڿ', 
	'5', '3', '6', '9', '~', '!', '@', '#', '$', '%', '&', 
	'*', '+', '=', '?' , ':)', ':(', ':P', ';)', '^_^',  
	'][', '>_<', '>_>', '::', 'oo' }
    n = math.random(#symbols)
    tag = awful.tag.add(symbols[n], {})
    awful.layout.set(layouts[1], tag)
    awful.tag.setscreen(tag, screen)
    awful.layout.arrange(screen)

    return tag
end


-- {{{ Tags
-- Define a tag table which hold all screen tags.
tags = {}
for s = 1, screen.count() do
    -- Each screen has its own tag table.
    -- Ɣ ʘ Δ δ Γ Ψ Ω ζ μ σ Ϟ Ϡ ϡ Ϩ Ю ֆ ջ چ ڤ ڿ
    -- need a way to make tags generate themselves with these symbols
    -- dynamically, on the fly
    --
    -- apparently this is how WMII handles it
    -- look into a modular config?

    tags[s] = awful.tag({}, s, layouts[1])
    tag = generate_tag(s)
    awful.tag.viewonly(tag)
end
